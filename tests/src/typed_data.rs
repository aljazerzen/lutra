use lutra_bin::{Value, typed_data};

#[track_caller]
fn _test_typed_data_roundtrip(value: Value, ty: &str) -> String {
    let (ty, ty_defs) = lutra_compiler::_test_compile_ty(ty);
    let data = value.encode(&ty, &ty_defs).unwrap();

    let mut ltd_buf = lutra_bin::bytes::BytesMut::new();
    typed_data::encode(&mut ltd_buf, &data, &ty, &ty_defs).unwrap();

    let (data_decoded, ty_decoded, ty_defs_decoded) = typed_data::decode(&ltd_buf).unwrap();

    let value_decoded = Value::decode(data_decoded, &ty, &ty_defs_decoded).unwrap();
    assert_eq!(value, value_decoded);
    assert_eq!(ty, ty_decoded);

    pretty_hex::pretty_hex(&ltd_buf)
}

